import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const vscodeDir = join(root, ".vscode");

const PRETTIER_CONFIG_FILES = [
  ".prettierrc",
  ".prettierrc.json",
  ".prettierrc.js",
  ".prettierrc.cjs",
  ".prettierrc.yaml",
  ".prettierrc.yml",
  "prettier.config.js",
  "prettier.config.cjs",
  "prettier.config.mjs",
];

const OXLINT_CONFIG_FILES = [".oxlintrc.json", "oxlint.json", "packages/config/oxlint.json"];

const FORMATTABLE_LANGUAGES = [
  "javascript",
  "typescript",
  "javascriptreact",
  "typescriptreact",
  "json",
  "jsonc",
];

function hasPrettier() {
  return PRETTIER_CONFIG_FILES.some((file) => existsSync(join(root, file)));
}

function hasOxlint() {
  if (OXLINT_CONFIG_FILES.some((file) => existsSync(join(root, file)))) {
    return true;
  }

  const packageJsonPath = join(root, "package.json");
  if (!existsSync(packageJsonPath)) {
    return false;
  }

  const pkg = JSON.parse(readFileSync(packageJsonPath, "utf8"));
  return Boolean(pkg.devDependencies?.oxlint || pkg.dependencies?.oxlint);
}

function languageFormatterSettings(formatterId) {
  return Object.fromEntries(
    FORMATTABLE_LANGUAGES.map((language) => [
      `[${language}]`,
      { "editor.defaultFormatter": formatterId },
    ]),
  );
}

function buildSettings(formatter) {
  const base = {
    "editor.formatOnSave": true,
    ...languageFormatterSettings(formatter.id),
  };

  if (formatter.kind === "prettier") {
    return {
      ...base,
      "editor.defaultFormatter": formatter.id,
    };
  }

  const settings = {
    ...base,
    "editor.defaultFormatter": formatter.id,
    "editor.formatOnSaveMode": "file",
    "editor.codeActionsOnSave": {
      "source.organizeImports": "never",
    },
    "oxc.enable.oxfmt": true,
    "oxc.enable.oxlint": true,
  };

  if (existsSync(join(root, ".oxlintrc.json"))) {
    settings["oxc.configPath"] = ".oxlintrc.json";
  }

  if (existsSync(join(root, ".oxfmtrc.json"))) {
    settings["oxc.fmt.configPath"] = ".oxfmtrc.json";
  }

  return settings;
}

function buildExtensions(formatter) {
  return {
    recommendations: [formatter.extensionId],
  };
}

const formatter = hasPrettier()
  ? {
      kind: "prettier",
      id: "esbenp.prettier-vscode",
      extensionId: "esbenp.prettier-vscode",
      label: "Prettier",
    }
  : hasOxlint()
    ? {
        kind: "oxc",
        id: "oxc.oxc-vscode",
        extensionId: "oxc.oxc-vscode",
        label: "Oxc (oxfmt)",
      }
    : null;

mkdirSync(vscodeDir, { recursive: true });

if (!formatter) {
  console.log("No .prettierrc or oxlint config found — skipping editor formatter setup.");
  process.exit(0);
}

const settingsPath = join(vscodeDir, "settings.json");
const extensionsPath = join(vscodeDir, "extensions.json");

const settingsHeader = `// Generated by scripts/sync-editor-settings.mjs — do not edit by hand.
// Re-run: bun run sync:editor
`;

writeFileSync(
  settingsPath,
  `${settingsHeader}${JSON.stringify(buildSettings(formatter), null, 2)}\n`,
);
writeFileSync(extensionsPath, `${JSON.stringify(buildExtensions(formatter), null, 2)}\n`);

console.log(`Editor formatter set to ${formatter.label}.`);
